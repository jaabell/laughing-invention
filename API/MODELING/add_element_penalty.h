int add_element_penalty(int ElementNumber, int node1, int node2, double penalty_stiffness, int dof)
{
    Element* theElement = 0;
    theElement = new PenaltyElement(ElementNumber, node1, node2, penalty_stiffness, dof);

    if (theElement == NULL)
    {
        cerr << "Error: (add_element_penalty) memory allocation problem for theElement!" << endl;
        return -1;
    }

    if (theDomain.addElement(theElement) == false)
    {
        cerr << "WARNING: (add_element_penalty) could not add element to the domain\n";
        cerr << "Element number: " << ElementNumber << endln;
        return -1;
    }

    return 0;

};

